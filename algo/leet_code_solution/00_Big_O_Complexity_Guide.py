# Example: Binary Search - O(log N)
def binary_search(arr, target):
    """
    Time: O(log N) - Halve search space each iteration
    Space: O(1) - Only pointers

    WHY O(log N)?
    Each comparison eliminates HALF of remaining array!
    N â†’ N/2 â†’ N/4 â†’ ... â†’ 1
    Steps to reach 1: logâ‚‚(N)
    """
    left, right = 0, len(arr) - 1

    while left <= right:           # â† At most log(N) iterations
        mid = (left + right) // 2

        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            left = mid + 1         # â† Cut LEFT half
        else:
            right = mid - 1        # â† Cut RIGHT half

    return -1


