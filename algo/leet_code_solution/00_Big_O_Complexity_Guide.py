# Example: Fibonacci - O(2^N) without memoization
def fibonacci_recursive(n):
    """
    Time: O(2^N) - Exponential due to repeated subproblems
    Space: O(N) - Recursion stack depth

    WHY O(2^N)?
    Each call makes 2 recursive calls
    Tree height = N
    Total nodes â‰ˆ 2^N

    âš ï¸ VERY SLOW! Use DP instead â†’ O(N)
    """
    if n <= 1:
        return n
    return fibonacci_recursive(n - 1) + fibonacci_recursive(n - 2)

# Example: Generate All Subsets - O(2^N)
def generate_subsets(nums):
    """
    Time: O(2^N) - Each element: include or exclude (2 choices)
    Space: O(2^N) - Store all 2^N subsets

    WHY O(2^N)?
    For each element: 2 choices (include or not)
    N elements â†’ 2 Ã— 2 Ã— 2 Ã— ... (N times) = 2^N combinations
    """
    result = []

    def backtrack(index, current):
        if index == len(nums):
            result.append(current[:])
            return

        # Choice 1: Don't include nums[index]
        backtrack(index + 1, current)

        # Choice 2: Include nums[index]
        current.append(nums[index])
        backtrack(index + 1, current)
        current.pop()

    backtrack(0, [])
    return result

# Example: Generate All Permutations - O(N!)
def permute(nums):
    """
    Time: O(N!) - Generate all N! permutations
    Space: O(N!) - Store all permutations

    WHY O(N!)?
    Position 1: N choices
    Position 2: N-1 choices
    Position 3: N-2 choices
    ...
    Total: N Ã— (N-1) Ã— (N-2) Ã— ... Ã— 1 = N!
    """
    result = []

    def backtrack(current):
        if len(current) == len(nums):
            result.append(current[:])
            return

        for num in nums:
            if num not in current:
                current.append(num)
                backtrack(current)
                current.pop()

    backtrack([])
    return result


