# Example: BST Search - O(log N) balanced, O(N) unbalanced
def search_bst(root, target):
    """
    Time: O(log N) for balanced tree, O(N) for unbalanced
    Space: O(log N) for balanced (recursion), O(N) for unbalanced

    WHY O(log N) balanced?
    Each comparison eliminates HALF of remaining tree!
    Similar to binary search: height = log(N)

    WHY O(N) unbalanced?
    Tree becomes linked list, must check all N nodes!
    """
    if not root:
        return None

    if root.val == target:
        return root
    elif target < root.val:
        return search_bst(root.left, target)   # â† Go left
    else:
        return search_bst(root.right, target)  # â† Go right

    # Balanced: at most log(N) recursive calls
    # Unbalanced: at most N recursive calls

# Example: Tree Height - O(N) time, O(log N) space for balanced
def tree_height(root):
    """
    Time: O(N) - Visit ALL nodes once (can't skip any!)
    Space: O(log N) balanced, O(N) unbalanced - recursion depth

    WHY O(N) time?
    Must visit EVERY node to compute height of subtrees!

    WHY O(log N) space for balanced?
    Recursion depth = height = log(N) for balanced tree!
    """
    if not root:
        return 0
    return 1 + max(tree_height(root.left), tree_height(root.right))


