# Example: Merge Sort - O(N log N)
def merge_sort(arr):
    """
    Time: O(N log N) - Divide and conquer
    Space: O(N) - Temporary arrays for merging

    WHY O(N log N)?
    - Recursion depth: log(N) levels (keep halving)
    - Work per level: O(N) to merge all elements
    - Total: log(N) Ã— N = O(N log N)
    """
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left = merge_sort(arr[:mid])     # â† Recurse on left half
    right = merge_sort(arr[mid:])    # â† Recurse on right half

    return merge(left, right)        # â† O(N) to merge

def merge(left, right):
    """O(N) to merge two sorted arrays"""
    result = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])
    return result


