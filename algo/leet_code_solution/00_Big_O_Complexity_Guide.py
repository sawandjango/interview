# Example: Single Loop - O(N)
def find_max_single_loop(arr):
    """
    Time: O(N) - One pass through array
    Space: O(1)

    WHY O(N)?
    Loop N times, each iteration O(1)
    N Ã— O(1) = O(N)
    """
    max_val = arr[0]
    for num in arr:        # â† N iterations
        if num > max_val:  # â† O(1) comparison
            max_val = num
    return max_val

# Example: Two Pointers - Still O(N)!
def two_sum_sorted(arr, target):
    """
    Time: O(N) - Each pointer moves at most N positions TOTAL
    Space: O(1)

    WHY O(N) with TWO pointers?
    left moves right at most N times
    right moves left at most N times
    Total moves: N (not 2N!)
    Each element visited AT MOST ONCE
    """
    left, right = 0, len(arr) - 1

    while left < right:  # â† At most N iterations combined
        curr_sum = arr[left] + arr[right]
        if curr_sum == target:
            return [left, right]
        elif curr_sum < target:
            left += 1   # â† Move left pointer
        else:
            right -= 1  # â† Move right pointer

    return None


