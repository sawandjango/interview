    result = []

    def dfs(node):
        visited.add(node)        # â† Mark visited: O(1)
        result.append(node)

        for neighbor in graph[node]:  # â† Check all edges from this node
            if neighbor not in visited:
                dfs(neighbor)

    dfs(start)
